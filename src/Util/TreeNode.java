public class TreeNode {
    public int val;
    public TreeNode left;
    public TreeNode right;
    public TreeNode parent;
    public TreeNode next;
    public TreeNode(int x) {
        val = x;
        left = null;
        right = null;
        parent = null;
        next = null;
    }
}
